public class PlanetasNeutrales {
    
    private String nombre;
    private String naves;
    private String produccion;
    private String porcentajeMuertes;
    private String posicionX;
    private String posicionY;

    public String getPosicionX() {
        return posicionX;
    }

    public void setPosicionX(String posicionX) {
        this.posicionX = posicionX;
    }

    public String getPosicionY() {
        return posicionY;
    }

    public void setPosicionY(String posicionY) {
        this.posicionY = posicionY;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNaves() {
        return naves;
    }

    public void setNaves(String naves) {
        this.naves = naves;
    }

    public String getProduccion() {
        return produccion;
    }

    public void setProduccion(String produccion) {
        this.produccion = produccion;
    }

    public String getPorcentajeMuertes() {
        return porcentajeMuertes;
    }

    public void setPorcentajeMuertes(String porcentajeMuertes) {
        this.porcentajeMuertes = porcentajeMuertes;
    }
    
}
